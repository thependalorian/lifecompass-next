// Helper functions to distinguish between customers and advisors
export function isCustomerPersona(personaId: string): boolean {
  return personaId.startsWith("CUST-");
}

export function isAdvisorPersona(personaId: string): boolean {
  return personaId.startsWith("ADV-");
}

export function getPersona(personaId: string): any {
  if (isCustomerPersona(personaId)) {
    return customerPersonas[personaId];
  } else if (isAdvisorPersona(personaId)) {
    return advisorPersonas[personaId];
  }
  return null;
}

export function getAllCustomerPersonas(): any[] {
  return Object.values(customerPersonas);
}

export function getAllAdvisorPersonas(): any[] {
  return Object.values(advisorPersonas);
}

